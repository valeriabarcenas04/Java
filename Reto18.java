public class Reto18 {
    public static void main(String[] args) {

        double radio = 5.0;

        double area = Math.PI * Math.pow(radio, 2);
        double perimetro = 2 * Math.PI * radio;

        System.out.println("Radio: " + radio);
        System.out.println("Área: " + area);
        System.out.println("Perímetro: " + perimetro);
    }
}
