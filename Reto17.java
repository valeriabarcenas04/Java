public class Reto17 {
    public static void main(String[] args) {
        int n = 17;
        int m = 5;

        int c = n / m; 
        int r = n % m; 

        System.out.println(n + " entre " + m +
                           " da un cociente " + c +
                           " y un residuo " + r);
    }
}
